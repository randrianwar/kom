		<h1> Rancangan Strategi </h1>
		<p>
			<span class="dropcap">S</span>ejalan dengan Visi Direktorat Jenderal Sumber Daya dan Perangkat Pos dan Informatika yaitu
			“terciptanya pembinaan penyelenggaraan pos, telekomunikasi dan informatika yang dinamis dengan peran aktif seluruh potensi
			nasional”, maka peran Balai Monitor SFR Kelas II Makassar selaku UPT Ditjen SDPPI dalam mewujudkan Visi dimaksud,
			menetapkan Visi sebagai berikut : “terwujudnnya penggunaan Spektrum Frekuensi Radio yang tertib, efesien dan bebas dari
			gangguan (interferensi) melalui penerapan sistem monitoring yang terintegrasi dengan SIM-S sebagai fungsi pengawasan yang
			profesional”
		</p>
		<br><br>
		<h2>MISI</h2>
		<p>
			Agar tujuan organisasi  yang ingin dicapai tersebut dapat terwujud dan berhasil dengan baik, maka diperlukan adanya misi
			yang harus dilaksanakan. Dalam hal ini misi yang ingin dilaksanakan oleh Balai Monitor Spektrum Frekuensi Radio Kelas II
			Makassar adalah :
		</p>
		<br><br>
		<ol>
			<li>Melakukan pengawasan melalui kegiatan monitoring dan penertiban frekuensi radio yang terintegrasi dengan
			  operasional SIM-S yang menjadi link sistem manajemen Direktorat Jenderal Sumber Daya, Perangkat Pos dan Informatika.</li>
			<li>Meningkatkan kualitas sarana monitoring dan perangkat SIM-S melalui kegiatan pemeliharaan dan perbaikan perangkat. </li>
			<li>Meningkatkan kualitas Sumber Daya Manusia (SDM) guna menunjang pencapaian kinerja terhadap pengawasan
			  penggunaan spektrum frekuensi radio di Sulawesi Selatan.</li>
			<li>Melakukan koordinasi dan dan sikronisasi terhadap instansi terkait, dalam mendukung terlaksananya tugas pokok.</li>
		</ol>
		<br><br>
		<h2>MAKSUD dan TUJUAN</h2>
		<p>Maksud dan Tujuan merupakan penjabaran/implementasi dari pernyataan misi dan merupakan sesuatu yang akan dicapai atau
			dihasilkan dalam jangka waktu tertentu.  Adapun maksud dan tujuan yang akan dicapai dalam pelaksanaan tugas pokok dan
			fungsi Balai Monitor Makassar adalah sebagai berikut :</p>
		<ol>
			<li>Menjamin penggunaan spektrum frekuensi radio sesuai dengan peruntukan dan tidak saling menganggu melalui
				kegiatan pengukuran parameter teknis di seluruh wilayah Sulawesi Selatan;</li>
			<li>Memberikan kenyamanan bagi pengguna frekuensi radio dan pengguna jasa frekuensi radio melalui kegiatan
				observasi monitoring frekuensi radio dan penanganan gangguan frekuensi radio;</li>
			<li>Meningkatkan kapabilitas dan profesionalisme SDM dalam pelaksanaan tupoksi guna menjawab tantangan
				perkembangan teknologi informasi dan komunikasi;</li>
			<li>Meningkatkan kualitas dan kuantitas fasilitas monitoring frekuensi radio;</li>
			<li>Meningkatkan pengendalian dan penindakan hukum atas penyalahgunaan penggunaan spektrum frekuensi
				radio melalui operasi penertiban frekuensi radio;</li>
			<li>Meningkatkan PNBP subsektor telekomunikasi melalui pelaksanaan pembinaan,  pelayanan dan penegakan
				hukum kepada penyelenggara dan pengguna spektrum frekuensi radio.</li>
		</ol>
		<br><br>
		<h2>SASARAN</h2>
		<ol>
		<li>Adanya ketersediaan spektrum untuk kepentingan umum, teknologi dan jasa-jasa baru, melalui
			pengaturan penggunaannya secara efesien dan efektif untuk memberi manfaat yang sebesar-besarnya bagi
			masyarakat dan user frequency di Prop. Sulawesi Selatan.</li>
			<li>Penggunaan spektrum frekuensi radio yang bebas dari interferensi dan sesuai peruntukan melalui upaya
				pengawasan teknis dan penegakan hukum di Prop. Sulawesi Selatan.</li>
		</ol>
		<br><br>
		<h2>KEBIJAKAN</h2>
		<p>Dalam rangka tercapainya sasaran, maka kebijakan-kebijakan strategis dititikberatkan pada upaya pelayanan prima
			kepada masyarakat dan user frequency secara berkesinambungan meningkatkan kuantitas dan kualitas penanganan gangguan,
			pemantauan dan identifikasi dini timbulnya pelanggaran teknis, pengembangan sumber daya manusia, penguatan koordinasi
			dengan instansi terkait, yang pada dasarnya dapat dirinci sebagai berikut :</p>
			<ol>
				<li>Menyusun rencana strategis sesuai kebutuhan pengembangan SDM</li>
				<li>Pemantauan dan Penanganan gangguan yang terukur sesuai SOP dengan indikator kecepatan, ketepatan dan keakuratan dalam penyelesaiannya.</li>
				<li>Membangun database hasil monitoring yang terintegrasi dengan fungsi SIM-S.</li>
				<li>Mengembangkan sarana pengendalian frekuensi radio yang tepat guna sesuai kondisi di Sulawesi Selatan.</li>
				<li>Meningkatkan kinerja secara continue dari aspek kompetensi SDM sesuai tugas yang diemban, sikap disiplin dan tanggung jawab.</li>
				<li>Meningkatkan komunikasi, koordinasi dan sinkronisasi internal dan eksternal</li>
				<li>Melaksanakan pengawasan dan penegakan hukum berdasarkan asas manfaat.</li>
			</ol>
