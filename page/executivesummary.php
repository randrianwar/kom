      <h1>Executiv Summary</h1>
    <div class="one-third"><img src="style/images/art/about.jpg" alt=""></div>
    <div class="two-third last">

      <p><span class="dropcap">A</span>zas manfaat merupakan dasar utama implementasi Undang-undang Nomor 36 tahun 1999
        tentang Telekomunikasi, hal ini berarti penyelenggaraan Telekomunikasi ataupun penggunaan spektrum frekuensi radio,
        diarahkan untuk meningkatkan kesejahteraan dan kemakmuran rakyat, melalui ketersediaan spektrum frekuensi radio yang
        berdaya guna serta terciptanya penggunaan spektrum frekuensi radio yang tertib, efisien, efektif, sesuai
        peruntukannya dan tidak saling menggangu. Oleh karena itu, untuk mewujudkan sasaran dimaksud perlu diatur sesuai
        dengan kerangka regulasi tertentu agar bisa bermanfaat dengan optimal. Salah satu mata rantai manajemen Spektrum
        Frekuensi Radio yang difokuskan mengawal undang-undang tersebut diatas, diemban oleh Balai Monitor Spektrum Frekuensi
        Radio Kelas II Makassar yang berperan dalam melaksanakan tugas-tugas inspeksi melalui pelaksanaan pengawasan dan
        pengendalian spektrum frekuensi radio.</p>
      <br>
      <p>Secara spesifik tugas pokok dan fungsi Balai Monitor Spektrum Frekuensi Radio Kelas II Makassar sebagai salah satu
        Unit Pelaksana Teknis (UPT) Direktorat SDPPI, diuraikan dalam Peraturan Menteri Komunikasi dan Informatika Nomor
        03/PER/M.KOMINFO/03/2011 tentang Organisasi dan Tata Unit Pelaksana Teknis Bidang Monitor Spektrum Frekuensi Radio
        meliputi; urusan tata usaha dan kerumahtanggaan termasuk penyusunan rencana dan program, kegiatan pengamatan,
        deteksi sumber pancaran, pengukuran stasiun radio, pemantauan atau monitoring dan penertiban frekuensi radio,
        penyediaan suku cadang, pemeliharaan dan perbaikan perangkat, verifikasi dan validasi data pengguna frekuensi radio,
        evaluasi dan pengujian ilmiah, pelayanan pengaduan masyarakat, serta penanganan gangguan penggunaan spektrum
        frekuensi radio.</p><br><br>
        </div>

      <p>Menyadari pentingnya arah pembinaan yakni pengawasan terhadap pengguna frekuensi radio, maka perkuatan Balai Monitor
        Spektrum Frekuensi Radio Kelas II Makassar ditopang pada tiga pilar dalam mendukung tercapainya sasaran yang telah
        ditetapkan yakni : penugasan SDM sesuai kompetensinya, optimalisasi perangkat monitoring dan SIMS yang terintegrasi dan
        berpedoman pada Visi dan Misi yang telah ditetapkan.</p>
      <br>
      <p>Capaian kinerja adalah Realisasi Program Kerja yang berhasil dilaksanakan tercermin dalam laporan akuntabilitas
        keuangan dan laporan realisasi penetapan kinerja tahun anggaran 2014 meliputi : pemeliharaan stasiun monitoring
        bergerak, pemeliharaan perangkat minitoring portabel dan peralatan pendukung, kalibrasi alat pengukuran, pemeliharaan
        kendaraan monitoring, perbaikan catu daya dan antena, monitoring dan observasi pendudukan spektrum frekuensi radio,
        pengukuran parameter teknis dan atas permintaan, deteksi sumber pancaran, evaluasi dan penanganan gangguan, pengamatan
        rutin frekuensi marabahaya untuk keselamatan jiwa manusia, kegiatan penertiban/penegakan hukum atas pelanggaran tindak
        pidana tertentu, penertiban dalam rangka eventpenting, peningkatan kualitas sumber daya manusia, penyampaian SPP BHP
        frekuensi radio, pembianaan ujian amatir dan pendistribusian IKRAP, memberi masukan teknis dalam EDP/FRB/EUCS dalam
        tahapan proses perizinan dinas siaran, pelaksanaan verifikasi dan validasi data konsesi/seluler/dinas siaran/maritim,
        pengelolaan dan pengawasan bidang kepegawaian dan keuangan, pemeliharaan terhadap sarana dan prasarana perkantoran,
        pengadaan barang dan jasa pemerintah, mendukung kegiatan-kegiatan Direktorat Jendral SDPPI yang dilaksanakan diwilayah
        Sulawesi Selatan dan Sulawesi Barat, serta meningkatkan koordinasi dan sinkronisasi terhadap instansi terkait dalam
        mendukung pelaksanaan tupoksi.</p>
        <br>
        <p>Dalam realisasi program dan penetapan kinerja tergambar hubungan input dan output kegiatan utama yang telah
        dilaksanakan, dimana memperlihatkan keterkaitan antara pembiayaan dan realisasi fisik sebagai hasil yang diharapkan,
        termasukefisiensi dalam pencapaian keluaran (rasio realisasi fisik dan keuangan).</p>
        <br>
        <p>Kinerja Balai Monitor Spektrum Frekuensi Radio Kelas II Makassar sebagaimana tertuang dalam rekonsiliasi laporan
          realisasi anggaran posisi 31 Desember 2014 penyerapannya mencapai 92,93 %.  Hal ini menandakan indikator
          keseluruhan program-program kerja berhasil dilaksanakan dengan baik, selain sangat tinggi dalam penyerapan juga
          indikasi baik dalam pengelolaan pertanggung jawaban keuangan yang didasarkan atas hasil audit reguler dan evaluasi
          keuangan akhir tahun anggaran 2014 oleh Inspektorat Jendral Kemkominfo.</p>
        <br>
        <p>Tentunya didalam pengelolaannya yang meskipun secara keseluruhan program kerja Balai Monitor Spektrum Frekuensi
          Radio Kelas II Makassar dapat dilaksanakan dengan baik, masih terdapat kendala dan permasalahan dalam pelaksanaan
          program. Kendala dan permasalahan ini diharapkan selalu ada solusi guna meningkatkan kinerjayang lebih baik untuk
          tahun anggaran yang akan datang.</p>
