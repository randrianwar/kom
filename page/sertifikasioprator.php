    <h2>Sertifikasi Radio Elektronika dan Oprator Radio</h2><br>
    <h5>Landasan Yuridis Sertifikasi Radio Elektronika dan Operator Radio</h5>
    <ul>
      <li>Undang-Undang Nomor 36 Tahun 1999 Tentang Telekomunikasi;</li>
      <li>Radio Regulation (RR) ITU</li>
      <li>Artikel 47 Peraturan Radio International (ITU) edisi tahun 2001;</li>
      <li>Konvensi Internasional IV/2 STCW 1978 yang diamandemen tahun 1995 dan tahun 2010;</li>
      <li>Permen Kominfo No. 02/Kominfo/2011.</li>
    </ul>
    <br><br>
    <h5>Kategori Sertifikasi Radio Elektronika dan Operator Radio</h5>
    <h6>1. Sertifikat Operator Radio Elektronika Kelas I</h6>
    <p>Sertifikat ini dikeluarkan menurut artikel 47 Peraturan Radio edisi tahun 2001 dan Konvensi Internasional IV/2 SCTW
      1978, yang telah dirubah tahun 1995 dan tahun 2010, dan sesuai rekomendasi Organisasi Maritim Internasional untuk
      pelatihan Personal Radio dan Sistem Marabahaya dan Keselamatan Global.<br>
      Pemilik Sertifikat Radio Elektronika Kelas I berwenang melakukan pemeliharaan dan perbaikan perangkat dan atau
      menyelenggarakan Dinas Telepon Radio pada Stasiun Radio Kapal dan Stasiun Radio Bumi Kapal dalam wilayah/area A1, A2,
      A3, dan A4 GMDSS.</p>
      <br>
    <h6>2. Sertifikat Operator Radio Elektronika Kelas II</h6>
    <p>Sertifikat ini dikeluarkan menurut artikel 47 Peraturan Radio edisi tahun 2001 dan Konvensi Internasional IV/2 STCW
      1978, yang telah dirubah tahun 1995, dan tahun 2010 dan sesuai rekomendasi Organisasi Maritim Internasional (IMO) untuk
      pelatihan Personal Radio dalam Sistem Marabahaya dan Keselamatan Global.<br>
      Pemegang sertifikat Redio Elektronika Kelas II berwenang melakukan pemeliharaan perangkat dan atau menyelenggarakan
      Dinas Telepon Radio pada Stasiun Radio Kapal dan Stasiun Bumi Kapal dalam wilayah / area A1, A2, A3, dan A4 GMDSS.</p>
      <br>
    <h6>3. Sertifikat Operator Umum</h6>
    <p>Sertifikat ini dikeluarkan menurut artikel 47 Peraturan Radio edisi tahun 2001 dan Konvensi Internasional IV/2
      STCW 1978, yang telah dirubah tahun 1996 dan tahun 2010, dan sesuai rekomendasi Organisasi Maritim Internasional
      (IMO) untuk pelatihan Personal Radio dalam Sistem Marabahaya dan Keselamatan Global.<br>
      Pemegang sertifikat ini berwenang untuk melakukan pekerjaan operator radio di kapal yang berlayar di area A1, A2, A3,
      A4 yang dilengkapi dengan perangkat GMDSS dan Operator Radio Pantai.</p>
      <br>
    <h6>4. Sertifikat Operator Terbatas</h6>
    <p>Sertifikat ini dikeluarkan menurut artikel 47 Peraturan Radio edisi tahun 2001 dan Konvensi Internasional IV/2 SCTW
      1978, yang telah dirubah tahun 1995, dan sesuai rekomendasi Organisasi Maritim Internasional untuk pelatihan Personal
      Radio dalam sistem Marabahaya dan Keselamatan Global.<br>
      Pemegang sertifikat ini berwenang untuk melakukan pekerjaan operator radio di kapal yang berlayar di area A1, yang
      dilengkapi dengan perangkat GMDSS.<br>
      Sertifikasi Operator Radio berlaku selama 20 tahun dengan kewajiban untuk memperpanjang setiap 5 tahun sekali.</p>
      <br><br>
    <h5>Sanksi Pelanggaran Frekuensi Radio</h5>
    <p>Barang siapa yang menggunakan frekuensi radio tanpa izin dan atau tidak sesuai dengan peruntukannya, maka akan
      dipidana dengan pidana penjara paling lama 4 (empat) tahun dan atau denda paling banyak Rp. 400.000.000,00
      (empat ratus juta rupiah) sesuai dengan Undang-Undang Telekomunikasi No. 36 Tahun 1999.</p>



