    <h2>Izin Spektrum Frekuensi Radio</h2>
    <p>Spektrum frekuensi radio merupakan sumber daya alam yang terbatas, mempunyai nilai strategis dan ekonomis yang tinggi
      serta dikelola dan dikuasai oleh negara.</p>
    <br>
    <p>Penggunaan spetrum frekuensi radio antara lain untuk sistem komunikasi radio Microwave Link. Penetepan frekuensi
      radio Microwave Link dilakukan berdasarkan analisa teknis ketersediaan kanal frekuensi radio dengan prinsip first-come
      first-served.</p>
    <br>
    <p>Dalam rangka efisiensi penggunaan spektrum frekuensi radio, penetapan izin penggunaan frekuensi radio atau Izin
      Stasiun Radio (ISR) untuk Microwave Link dapat diberikan kepada penyelenggara jaringan telekomunikasi. Badan hukum
      atau instansi pemerintah yang memerlukan sistem komunikasi radio Microwave Link dapat menyewa kepada penyelenggara
      jaringan telekomunikasi.</p>
