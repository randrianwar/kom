    <h2>Sertifikasi Alat dan Perangkat Telekomunikasi</h2>
    <h5>Tujuan Sertifikasi </h5>
    <p>Tujuan pemenuhan persyaratan teknis sesuai Peraturan Pemerintah No. 52 Tahun 2000 tentang Penyelenggaraan Telekomunikasi :</p>
    <ul>
      <li>Menjamin keterhubungan dalam jaringan telekomunikasi;</li>
      <li>Mencegah saling mengganggu antara alat dan perangkat telekomunikasi;</li>
      <li>Melindungi masyarakat dari kemungkinan kerugian yang ditimbulkan akibat pemakaian alat dan perangkat telekomunikasi;</li>
      <li>Mendorong berkembangnya industri, inovasi dan rekayasa teknologi telekomunikasi nasional.</li>
    </ul>
    <br>
    <h5>Prosedur Sertifikasi dan Label</h5>
    <h6>1. Sertifikasi</h6>
    <p>Prosedur dan proses sertifikasi perlu diketahui dan dimengerti oleh para pemohon (pabrikan/importir) agar terciptanya efisiensi waktu,
      biaya dan tenaga.<br>
      Segala persyaratan yang dibutuhkan untuk proses sertifikasi hendaknya dilengkapi terlebih dahulu secara lengkap oleh
      pemohon, untuk mempermudah proses sertifikasi.<br>
      Permohonan dapat dilakukan secara online di <a href="http://sertifikasi.postel.go.id">sertifikasi.postel.go.id</a> dan
      tetap berdasarkan persyaratan dan ketentuan yang berlaku.</p>
      <br>
      <h6>1. Label</h6>
      <p>Pemegang sertifikat wajib memberikan label pada setiap alat/perangkat telekomunikasi yang telah bersertifikat serta
        pada kemasannya dan melaporkan pelaksanaan pelabelan kepada Direktorat Jenderal Sumber Daya dan Perangkat Pos dan
        Informatika (Ditjen SDPPI) up. Direktur Standardisasi Perangkat Pos dan Informatika paling lambat 30 (tigapuluh)
        hari setelah diterbitkan sertifikat.<br>
        Dalam hal ini, label tidak dapat dilekatkan pada alat dan perangkat telekomunikasi maka label wajib dilekatkan pada
        kemasan, pembungkus atau buku manual alat dan perangkat telekomunikasi tersebut.</p>
